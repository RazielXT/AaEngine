DXGI_FORMAT StringToDxgiFormat(const std::string& str)
{
	static std::unordered_map<std::string, DXGI_FORMAT> map = [] {
		std::unordered_map<std::string, DXGI_FORMAT> m;
		for (auto& p : gFormatTable)
			m[p.first] = p.second;
		return m;
		}();

	auto it = map.find(str);
	return (it != map.end()) ? it->second : DXGI_FORMAT_UNKNOWN;
}

const char* DxgiFormatToString(DXGI_FORMAT fmt)
{
	static std::unordered_map<DXGI_FORMAT, const char*> map = [] {
		std::unordered_map<DXGI_FORMAT, const char*> m;
		for (auto& p : gFormatTable)
			m[p.second] = p.first;
		return m;
		}();

	auto it = map.find(fmt);
	return (it != map.end()) ? it->second : "DXGI_FORMAT_UNKNOWN";
}
